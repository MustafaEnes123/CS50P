def main():
    interests = input("What are your interests? ")
    print(f"Your interests are: {interests}")
    if interests == "programming":
        print("That's great! Programming is a valuable skill.")
    elif interests == "sports":
        print("Sports are a great way to stay active and healthy.")
    elif interests == "music":
        print("Music is a wonderful form of expression and creativity.")
    else:
        print("That's interesting! It's great to have diverse interests.")
main()
